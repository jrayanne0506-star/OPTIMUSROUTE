/**
 * Retorna as coordenadas de uma quadra, ou null se não encontrada.
 * Normaliza o input (ex: "qnl07" → "QNL 07")
 */
export function getCoordenadas(quadra) {
  const normalizada = quadra
    .trim()
    .toUpperCase()
    .replace(/([A-Z\/]+)(\d)/, "$1 $2");
  return COORDENADAS_QUADRAS[normalizada] ?? null;
}